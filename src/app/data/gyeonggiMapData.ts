// 프로젝션 함수: 경위도 → SVG 좌표
export function projectCoords(
  coords: [number, number][],
  config = { originLon: 126.10, originLat: 38.60, scaleX: 310, scaleY: 390 }
): string {
  return coords
    .map(([lon, lat]) => {
      const x = (lon - config.originLon) * config.scaleX;
      const y = (config.originLat - lat) * config.scaleY;
      return `${x.toFixed(1)},${y.toFixed(1)}`;
    })
    .join(' ');
}

// 폴리곤 중심점 계산
export function getPolygonCenter(
  coords: [number, number][],
  config = { originLon: 126.10, originLat: 38.60, scaleX: 310, scaleY: 390 }
): { x: number; y: number } {
  let sumX = 0;
  let sumY = 0;
  for (const [lon, lat] of coords) {
    sumX += (lon - config.originLon) * config.scaleX;
    sumY += (config.originLat - lat) * config.scaleY;
  }
  return {
    x: sumX / coords.length,
    y: sumY / coords.length,
  };
}
